def classify_variable(var_name, var_type):
    print(f"{var_name}: {var_type}")

print("### Classification of Variables ###\n")
classify_variable("Number of pets", "Quantitative (Discrete)")
classify_variable("Blood type", "Qualitative (Categorical)")
classify_variable("Weight in kg", "Quantitative (Continuous)")
classify_variable("Satisfaction level", "Qualitative (Categorical)")
